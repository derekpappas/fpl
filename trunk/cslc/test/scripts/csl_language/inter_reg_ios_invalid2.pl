sub testDirExists {
    my($execName) = @_;
    unless(-e "$execName") { die "[ERROR] The directory $execName does not exist!\n"; }
    
}

sub mkDir {
    my($dirName) = @_;
    unless(-e "$dirName")  { `mkdir $dirName`; }
}

foreach(my $i = 1; $i <= 300; $i++) {
    &main($i);
}

sub main {
    my $i = $_[0];
    #my @dir = ("input","output","inout");
    #my $l = int(rand($#dir+1));
    my @sp = ("  csl_port clk(input);","  csl_signal clk(reg);");
    my $test_name = "inter_reg_ios".$i."_illegal.csl";
   	&createCslFile($test_name, $i, @sp);
}

sub createCslFile {
    my ($test_name, $i, @sp) = @_;
    my $path = "$ENV{WORK}/test/csl_test_gen/";
    my $k = int(rand(20)) + int(rand(20));
    my $zero = 0;
    my $one = 1;
    my $two = 2;
    my $tre = 3;
    my $val1 = int(rand(100))+1;
    my $val2 = int(rand(100))+2;
    my $val3 = int(rand(100))+3;
    my $m = $k + 5;
    if($test_name=~/(_legal)/){
        &mkDir("$path/inter_reg_ios_valid");
        open(FH,">$path/inter_reg_ios_valid/$test_name");
    }
    elsif($test_name=~/(_illegal)/) {
        &mkDir("$path/inter_reg_ios_invalid2"); 
        open(FH,">$path/inter_reg_ios_invalid2/$test_name");
    }
    print FH "//Generated by oanab\n\n";
    print FH "//Test cases from: register_ios_matr.ods\n";

#interface ifck
    print FH "csl_interface ifc$k {\n";
    print FH "  csl_port p_x$i(input,$val2);\n";
    print FH "  csl_port p_y$i(output,$val3);\n";
    print FH "  csl_port p_z$i(input,$val1);\n";
    print FH "  ifc$k () {\n";
    print FH "  }\n";
    print FH "};\n\n";
    
#interface ifcm
    if($i > 50 && $i <= 100) {
	print FH "csl_interface ifc$m {\n";
	print FH "  csl_port p_a$i(output,".($val1+3).");\n";
	print FH "  csl_port p_b$i(input,$val3);\n";
	print FH "  csl_port p_c$i(input,$val2);\n";
	print FH "  ifc$k ifc$k\_$zero;\n";
	print FH "  ifc$m () {\n";
	if($i > 50 && $i <= 100) {
	    print FH "    set_clock(clk);\n";
	    print FH "    register_ios(output);\n";
	}
	print FH "  }\n";
	print FH "};\n\n";
    }

#signal group sgk
    if($i > 200 && $i <= 250) {
	print FH "csl_signal_group sg$k {\n";
	print FH "  csl_signal s_a$i($val2);\n";
	print FH "  csl_signal s_b$i($val1);\n";
	print FH "  sg$k () {\n";
	print FH "  }\n";
	print FH "};\n\n";
    }

#register reg
    if($i > 250 && $i <= 300) {
	print FH "csl_register reg$k {\n";
	print FH "  reg$k () {\n";
	print FH "    set_width($val2);\n";
	print FH "    set_type(register);\n";
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
	print FH "  }\n";
	print FH "};\n\n";
    }

#register file rf
    print FH "csl_register_file rf$k {\n";
    print FH "  rf$k () {\n";
    print FH "    set_width($val3);\n";
    print FH "    set_depth(".($val1+$val3).");\n";
    if($i > 25 && $i <= 50) {
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
    }
    elsif($i > 100 && $i <= 200) {
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
    }
    print FH "  }\n";
    print FH "};\n\n";
    

#memory mem
    print FH "csl_memory mem$k {\n";
    print FH "  mem$k () {\n";
    print FH "    set_width($val2);\n";
    print FH "    set_depth(".($val2+$val3).");\n";
    if($i <= 25) {
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "    set_clock(clk);\n";
	print FH "    register_ios(output);\n";
    }
    print FH "  }\n";
    print FH "};\n\n";


#unit a
    print FH "csl_unit a$k {\n";
    print FH $sp[int rand($#sp+1)];
    print FH "\n";
    if($i <= 50) {
	print FH "  rf$k rf$k\_$zero;\n";
    }
    elsif($i > 50 && $i <= 150) {
	print FH "  rf$k rf$k\_$zero;\n";
	print FH "  mem$k mem$k\_$zero;\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "  csl_signal s_$i($val1);\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "  rf$k rf$k\_$zero;\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "  reg$k reg$k\_$zero;\n";
	print FH "  ifc$k ifc$k\_$zero;\n";
    }
    print FH "  a$k () {\n";
    print FH "    clk.set_attr(clock);\n";
    if($i <= 25) {
	print FH "    rf$k\_$zero.set_clock(clk);\n";
	print FH "    rf$k\_$zero.register_ios(output);\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "    rf$k\_$zero.set_clock(clk);\n";
	print FH "    rf$k\_$zero.register_ios(input);\n";
	print FH "    mem$k\_$zero.set_clock(clk);\n";
	print FH "    mem$k\_$zero.register_ios(output);\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "    s_$i.set_clock(clk);\n";
	print FH "    s_$i.register_ios(input,.en(enable$k));\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "    rf$k\_$zero.set_clock(clk);\n";
	print FH "    rf$k\_$zero.register_ios(input);\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "    ifc$k\_$zero.set_clock(clk);\n";
	print FH "    ifc$k\_$zero.register_ios(output);\n";
    }
    print FH "  }\n";
    print FH "};\n\n";


# unit b
    print FH "csl_unit b$k {\n";
    print FH "  a$k a$k\_$zero;\n";
    print FH $sp[int rand($#sp+1)];
    print FH "\n";
    if($i <= 50) {
	print FH "  mem$k mem$k\_$zero;\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  ifc$k ifc$k\_$zero;\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "  rf$k rf$k\_$zero;\n";
	print FH "  mem$k mem$k\_$zero;\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "  mem$k mem$k\_$one;\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "  rf$k rf$k\_$one;\n";
	print FH "  csl_signal enable$k;\n";
    }
    print FH "  b$k () {\n";
    print FH "    clk.set_attr(clock);\n";
    if($i > 25 && $i <= 50) {
	print FH "    mem$k\_$zero.set_clock(clk);\n";
	print FH "    mem$k\_$zero.register_ios(output);\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "    mem$k\_$zero.set_clock(clk);\n";
	print FH "    mem$k\_$zero.register_ios(output);\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "    mem$k\_$one.set_clock(clk);\n";
	print FH "    mem$k\_$one.register_ios(output);\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "    rf$k\_$one.set_clock(clk);\n";
	print FH "    rf$k\_$one.register_ios(output,.en(enable$k));\n";
    }
    print FH "  }\n";
    print FH "};\n\n";


# unit c
    print FH "csl_unit c$k {\n";
    print FH "  b$k b$k\_$zero;\n";
    print FH $sp[int rand($#sp+1)];
    print FH "\n";
    if($i <= 50) {
	print FH "  ifc$k ifc$k\_$zero;\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  ifc$m ifc$m\_$zero;\n";
	print FH "  csl_signal rst$k;\n";
    }
