#!/usr/bin/perl

#oanab
#connect_by_name() with op expr
#Test_Matrix: /test_matrices/ar_conn_by_name_matr.ods, 2 test cases (multiple op.expr);

use strict;

print "Generated tests path : test/csl_test_gen/ar_conn_name_op_valid2/\n";

if(! defined $ENV{WORK}){
    die "[ERROR] The environment variable WORK is not set. Set it to point to TOT/se";
}

my $totDir     = $ENV{WORK};
&testDirExists($totDir);

sub testDirExists {
    my($execName) = @_;
    unless(-e "$execName") { die "[ERROR] The directory $execName does not exist!\n"; }
    
}

sub mkDir {
    my($dirName) = @_;
    unless(-e "$dirName")  { `mkdir $dirName`; }
}

foreach(my $i = 1; $i <= 150; $i++) {
    &main($i);
}

sub main {
    my $i = $_[0];
    my $j = $_[1];
    my $m = $_[2];
    my $n = $_[3];
    $j = int(rand(20))+1; 
    $m = int(rand(20))+2; 
    $n = int(rand(20))+3; 
    my %dir = (1,"input",
	       2,"output",
	       3,"inout");
    my $dir = $_[4];
    my $test_name = "ar_conn_name_op".$i."_legal.csl";
    if($j != $m && $j != $n && $m != $n) {
	&createCslFile($test_name, $i, $j, $m, $n, %dir);
    }
    else {
	&createCslFile($test_name, $i, $j+1, $m+2, $n+3, %dir);
    }
}
sub createCslFile {
    my($test_name, $i, $j, $m, $n, %dir) = @_;
    my $path = "$ENV{WORK}/test/csl_test_gen/";
    my $k = int(rand(20)) + int(rand(20));
    my $zero = 0;
    my $one = 1;
    my $two = 2;
    my $tre = 3;
    my $val1 = int(rand(100))+3;
    my $val2 = int(rand(100))+3;
    my $val3 = int(rand(100))+8;
    my $l = int(rand(3))+1;
    if($test_name=~/(_legal)/){
        &mkDir("$path/ar_conn_name_op_valid2");
        open(FH,">$path/ar_conn_name_op_valid2/$test_name");
    }
    elsif($test_name=~/(_illegal)/) {
        &mkDir("$path/ar_conn_name_op_invalid"); 
        open(FH,">$path/ar_conn_name_op_invalid/$test_name");
    }
    print FH "//Generated by oanab\n\n";
    print FH "//Test_Matrix: /test_matrices/ar_conn_by_name_matr.ods\n\n";

#bitranges
    print FH "csl_bitrange br1($val1);\n\n";

    print FH "csl_bitrange br2($val2);\n\n";
    
    print FH "csl_bitrange br3($val3);\n\n";


#unit a
    print FH "csl_unit a$k {\n";
    if($i > 50 && $i <= 100) {
	print FH "  csl_port p_b$i(input,$val3);\n";
    }
    print FH "  a$k () {\n";
    print FH "  }\n";
    print FH "};\n\n";

#unit b
    print FH "csl_unit b$k {\n";
    if($i <= 50) {
	print FH "  a$k a$k\_$zero;\n";
	print FH "  csl_port p_x$i(input,$val2);\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  a$k a$k\_$zero;\n";
	print FH "  csl_signal s_x$i(".($val3).");\n";
	print FH "  csl_signal s_y$i(".($val3).");\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  a$k a$k\_$zero;\n";
	print FH "  csl_signal s$i(br3);\n";
    }
    print FH "  b$k () {\n";
#connect_by_name : port---op expr (CP)
    if($i > 50 && $i <= 100) {
	print FH "    a$k\_$zero.p_b$i.connect_by_name(s_x$i ^ s_y$i);  // port---op expr (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";

#unit c
    print FH "csl_unit c$k {\n";
    if($i <= 100) {
	print FH "  b$k b$k\_$zero;\n";
	print FH "  csl_port p_y$i(input,br2);\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  b$k b$k\_$zero;\n";
	print FH "  csl_port p$i(input,br3);\n";
    }
    print FH "  c$k () {\n";
#connect_by_name : port---op expr (CP)
    if($i <= 50) {
	print FH "    b$k\_$zero.p_x$i.connect_by_name(~p_y$i);  // port---op expr (CP)\n";
    }
#connect_by_name : sig---op expr (CP)
    elsif($i > 100 && $i <= 150) {
	print FH "    b$k\_$zero.s$i.connect_by_name(~p$i);  // sig---op expr (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";

#unit d
    print FH "csl_unit d$k {\n";
    if($i <= 50) {
	print FH "  c$k c$k\_$zero;\n";
	print FH "  csl_signal s_x$i(br2);\n";
	print FH "  csl_signal s_y$i($val2);\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  c$k c$k\_$zero;\n";
	print FH "  csl_signal s_x$i($val2);\n";
	print FH "  csl_signal s_y$i(br2);\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  c$k c$k\_$zero;\n";
	print FH "  csl_signal s_a$i($val3);\n";
	print FH "  csl_signal s_b$i(br3);\n";
    }
    print FH "  d$k () {\n";
#connect_by_name : port---op expr (CP)
    if($i <= 50) {
	print FH "    c$k\_$zero.b$k\_$zero.p_x$i.connect_by_name(s_x$i | s_y$i);  // port---op expr (CP)\n";
    }
#connect_by_name : port---op expr (CP)
    elsif($i > 50 && $i <= 100) {
	print FH "    c$k\_$zero.p_y$i.connect_by_name(s_x$i & s_y$i,pa_y$i);  // port---op expr (CP)\n";
    }
#connect_by_name : sig---op expr (CP)
    elsif($i > 100 && $i <= 150) {
	print FH "    c$k\_$zero.p$i.connect_by_name(s_a$i | s_b$i);  // sig---op expr (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";



}
