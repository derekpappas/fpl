template<class T>
int arrayLength(T* a)
{
    T* p;
    for(p = a; *p; p++)
       ;
    return p - a;
}
int arrayLength(char a);

int compare_letters(char l1, char l2)
{
   if(l1 == l2)
      return 1;
   else
      return 0;
}

int find_str(string source, string wanted)
{
   int match = 0, counter = 0;
   int sizeS = arrayLength(&source[0]);
   int sizeW = arrayLength(&wanted[0]);
   if(sizeS < sizeW)
   {
      //cout<<"Error!\nThe string you want to find is bigger then the string in which you want to search in!\n";
      return 0;
   }
   else
   {
      for(int i = 0; i <= sizeS - sizeW; i++)
      {
         for(int j = 0; j < sizeW; j++)
            counter = counter + compare_letters(source[i+j], wanted[j]);
         if(counter == sizeW)
         {
            match = 1;
            break;
         }
         counter = 0;
      }
      if(match == 1)
         return 1;
      else
         return 0;
   }
} 


