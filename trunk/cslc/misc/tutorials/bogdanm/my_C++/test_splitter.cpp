int main(int argc, char *argv[])
{
    char *output = "0000.v";
    ifstream source(argv[1]);//open the desired file
    if(source.fail())
    {
       cout<<"Error when opening file "<<argv[1]<<endl;
       //getch();
       exit(1);
    }
    int counter = 0;//initialize counter
    string line;//declare buffer for text lines
    vector<string> v;//vector that contains the lines of the file
    vector<string> temp;//temporary vector which will contain the content of each generated file
    string vline;
    string specialCaracter = "//@";
    while(getline(source, line))
       v.push_back(line); // store all the lines in this vector
    source.close();   
    /*for(int i = 0; i < v.size(); i++)
       cout << i << ": " << v[i] << endl;*/
    for(int i = 0; i < v.size(); i++)
    {
       vline = v[i];
       if(find_str(vline, specialCaracter) == 1)
       {
          //cout<<"Opening "<<output<<" for writing...\n";
          ofstream out(output);
          if(out.fail())
          {
             cout<<"Eror when opening file "<<output<<endl;
             counter++;
             output = modify_output(counter);
             continue;
          }
          for(int j = 0; j < temp.size(); j++)
             out<<temp[j]<<endl;
          out.close();
          temp.clear();
          counter++;
          cout<<"File "<<output<<" generated...\n";
          output = modify_output(counter); 
          continue;
       }
       temp.push_back(vline);
    }
    cout<<"A total of "<<counter<<" files have been generated from file "<<argv[1]<<endl;
    return EXIT_SUCCESS;
} 
