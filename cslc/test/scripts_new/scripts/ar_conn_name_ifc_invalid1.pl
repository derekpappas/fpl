#!/usr/bin/perl

#oanab
#connect_by_name() with ifc's

use strict;

print "Generated tests path : test/csl_test_gen/ar_conn_name_ifc_invalid1/\n";

if(! defined $ENV{WORK}){
    die "[ERROR] The environment variable WORK is not set. Set it to point to TOT/se";
}

my $totDir     = $ENV{WORK};
&testDirExists($totDir);

sub testDirExists {
    my($execName) = @_;
    unless(-e "$execName") { die "[ERROR] The directory $execName does not exist!\n"; }
    
}

sub mkDir {
    my($dirName) = @_;
    unless(-e "$dirName")  { `mkdir $dirName`; }
}

foreach(my $i = 1; $i <= 400; $i++) {
    &main($i);
}

sub main {
    my $i = $_[0];
    my $j = $_[1];
    my $m = $_[2];
    my $n = $_[3];
    $j = int(rand(20))+1; 
    $m = int(rand(20))+2; 
    $n = int(rand(20))+3; 
    my %dir = (1,"input",
	       2,"output",
	       3,"inout");
    my $dir = $_[4];
    my $test_name = "ar_conn_name_ifc".$i."_illegal.csl";
    if($j != $m && $j != $n && $m != $n) {
	&createCslFile($test_name, $i, $j, $m, $n, %dir);
    }
    elsif($j == $m) {
	&createCslFile($test_name, $i, $j+1, $m+2, $n, %dir);
    }
  }
sub createCslFile {
    my($test_name, $i, $j, $m, $n, %dir) = @_;
    my $path = "$ENV{WORK}/test/csl_test_gen/";
    my $k = int(rand(20)) + int(rand(20));
    my $zero = 0;
    my $one = 1;
    my $two = 2;
    my $tre = 3;
    my $val1 = int(rand(100))+2;
    my $val2 = int(rand(100))+3;
    my $val3 = int(rand(100))+8;
    my $l = int(rand(3))+1;
    if($test_name=~/(_legal)/){
        &mkDir("$path/ar_conn_name_ifc_valid");
        open(FH,">$path/ar_conn_name_ifc_valid/$test_name");
    }
    elsif($test_name=~/(_illegal)/) {
        &mkDir("$path/ar_conn_name_ifc_invalid1"); 
        open(FH,">$path/ar_conn_name_ifc_invalid1/$test_name");
    }
    print FH "//Generated by oanab\n\n";

#bitranges
    print FH "csl_bitrange br1($val1);\n\n";

    print FH "csl_bitrange br2($val2);\n\n";
    
    print FH "csl_bitrange br3($val3);\n\n";

#ifcj
    print FH "csl_interface ifc$j {\n";
    print FH "  csl_port ps_m$i($dir{$l},br1);\n";
    if($l < 3) {
	print FH "  csl_port ps_n$i($dir{$l+1},br3);\n";
    }
    else {
	print FH "  csl_port ps_n$i($dir{$l-1},br3);\n";
    }
    print FH "  csl_port ps_o$i(inout,$val1);\n";
    print FH "  ifc$j () {\n";
    print FH "  }\n";
    print FH "};\n\n";


#ifcm
    if($i > 50 && $i <= 250 || $i > 350 && $i <= 400) {
	print FH "csl_interface ifc$m {\n";
	print FH "  csl_port p_x$i($dir{$l},br2);\n";
	print FH "  ifc$j ifc$j\_$zero;\n";
	print FH "  ifc$j ifc$j\_$one;\n";
	print FH "  ifc$m () {\n";
	print FH "  }\n";
	print FH "};\n\n";
    }

#sgj
    if($i > 100 && $i <= 150 || $i > 200 && $i <= 250) {
	print FH "csl_signal_group sg$j {\n";
	print FH "  csl_signal s_a$i(".($val2+$val1).");\n";
	print FH "  csl_signal s_b$i(br1);\n";
	print FH "  csl_signal s_c$i(br3);\n";
	print FH "  sg$j () {\n";
	print FH "  }\n";
	print FH "};\n\n";
    }


#unit a
    print FH "csl_unit a$k {\n";
    print FH "  csl_port p_a$i(inout,br3);\n";
    if($i <= 50) {
	print FH "  ifc$j ifc$j\_$one;\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  ifc$m ifc$m\_$one;\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  ifc$j ifc$j\_$one;\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "  csl_port p_z$i($dir{$l},".($val1+$val2+$val3).");\n";
	print FH "  csl_signal s_z$i(br3);\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "  ifc$m ifc$m\_$zero;\n";
    }
    elsif($i > 300 && $i <= 350) {
	print FH "  ifc$j ifc$j\_$zero;\n";
    }
    print FH "  a$k () {\n";
    print FH "  }\n";
    print FH "};\n\n";

#unit b
    print FH "csl_unit b$k {\n";
    print FH "  a$k a$k\_$zero;\n";
    if($i <= 50) {
	print FH "  csl_signal s_x$i(".(2*$val2+$val3).");\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  a$k a$k\_$one;\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  sg$j sg$j\_$zero;\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "  ifc$m ifc$m\_$zero;\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "  sg$j sg$j\_$zero;\n";
	print FH "  ifc$j ifc$j\_$zero;\n";
    }
    elsif($i > 350 && $i <= 400) {
	print FH "  ifc$m ifc$m\_$zero;\n";
    }
    print FH "  b$k () {\n";
#connect_by_name : ifc.ifc---sg.sig (CP)
    if($i > 200 && $i <= 250) {
	print FH "    a$k\_$zero.ifc$m\_$zero.ifc$j\_$zero.connect_by_name(sg$j\_$zero.s_a$i,pa_s$i);  // ifc.ifc---sg.sig (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";

#unit c
    print FH "csl_unit c$k {\n";
    print FH "  b$k b$k\_$zero;\n";
    if($i <= 50) {
	print FH "  ifc$j ifc$j\_$zero;\n";
	print FH "  b$k b$k\_$one;\n";
    }
    elsif($i > 50 && $i <= 100) {
	print FH "  csl_port p_x$i($dir{$l},br3);\n";
	print FH "  csl_signal s_x$i(br2);\n";
    }
    elsif($i > 100 && $i <= 150) {
	print FH "  ifc$j ifc$j\_$zero;\n";
    }
    elsif($i > 150 && $i <= 200) {
	print FH "  a$k a$k\_$one;\n";
	print FH "  a$k a$k\_$two;\n";
    }
    elsif($i > 250 && $i <= 300) {
	print FH "  ifc$j ifc$j\_$zero;\n";
    }
    elsif($i > 300 && $i <= 400) {
	print FH "  csl_signal s_a$i(br1);\n";
	print FH "  csl_signal s_b$i(br3);\n";
	print FH "  ifc$j ifc$j\_$one;\n";
    }
    print FH "  c$k () {\n";
#connect_by_name : ifc---sig (SS)
    if($i <= 50) {
	print FH "    b$k\_$zero.a$k\_$zero.ifc$j\_$one.connect_by_name(b$k\_$one.s_x$i);  // ifc---sig (SS)\n";
    }
#connect_by_name : ifc---p.ps (CP), ifc---sig.ps (CP)
    elsif($i > 50 && $i <= 100) {
	print FH "    b$k\_$zero.a$k\_$zero.ifc$m\_$one.connect_by_name(p_x$i\[".$val3."\:".($val3-4)."\],pa_x$i);  // ifc---p.ps (CP)\n";
	print FH "    b$k\_$zero.a$k\_$one.ifc$m\_$one.connect_by_name(s_x$i\[".$val2."\-\:".(4)."\]);  // ifc---sig.ps (CP)\n";
    }
#connect_by_name : ifc---sg.sig (PC)
    elsif($i > 100 && $i <= 150) {
	print FH "    ifc$j\_$zero.connect_by_name(b$k\_$zero.sg$j\_$zero.s_b$i,pb_s$i);  // ifc---sg.sig (PC)\n";
    }
#connect_by_name : ifc---sg (object that doesn't exist)
    elsif($i > 250 && $i <= 300) {
	print FH "    ifc$j\_$zero.connect_by_name(sg$j\_$one);  // ifc---sg (object that doesn't exist)\n";
    }
#connect_by_name : ifc---repl expr (CP)
    elsif($i > 300 && $i <= 350) {
	print FH "    b$k\_$zero.a$k\_$zero.ifc$j\_$zero.connect_by_name({3{s_a$i,s_b$i}});  // ifc---repl expr (CP)\n";
    }
#connect_by_name : ifc.ifc---concat expr (CP)
    elsif($i > 350 && $i <= 400) {
	print FH "    b$k\_$zero.ifc$m\_$zero.ifc$j\_$zero.connect_by_name({s_a$i,s_b$i});  // ifc.ifc---concat expr (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";

#unit d
    print FH "csl_unit d$k {\n";
    print FH "  c$k c$k\_$zero;\n";
    if($i <= 50) {
	print FH "  csl_port p_x$i($dir{$l},".(2*$val2+$val3).");\n";
    }
    elsif($i > 50 && $i <= 150) {
	print FH "  ifc$m ifc$m\_$two;\n";
    }
    elsif($i > 200 && $i <= 250) {
	print FH "  ifc$m ifc$m\_$one;\n";
    }
    elsif($i > 300 && $i <= 400) {
	print FH "  csl_port p_x$i(input,$val2);\n";
	print FH "  csl_signal s_x$i(br1);\n";
	print FH "  csl_signal s_y$i($val3);\n";
    }
    print FH "  d$k () {\n";
#connect_by_name : ifc---port (CP)
    if($i <= 50) {
	print FH "    c$k\_$zero.ifc$j\_$zero.connect_by_name(p_x$i,pa_x$i);  // ifc---port (CP)\n";
    }
#connect_by_name : ifc.ifc---p.ps (PC), ifc.ifc---sig.ps (PC)
    elsif($i > 50 && $i <= 100) {
	print FH "    ifc$m\_$two.ifc$j\_$zero.connect_by_name(c$k\_$zero.p_x$i\[".$val3."\:".($val3-4)."\]);  // ifc.ifc---p.ps (PC)\n";
	print FH "    ifc$m\_$two.ifc$j\_$one.connect_by_name(c$k\_$zero.s_x$i\[".$val2."\-\:".(4)."\]);  // ifc.ifc---sig.ps (PC)\n";
    }
#connect_by_name : ifc---ifc.ifc.p (CP)
    elsif($i > 100 && $i <= 150) {
	print FH "    c$k\_$zero.b$k\_$zero.a$k\_$zero.ifc$j\_$one.connect_by_name(ifc$m\_$two.ifc$j\_$zero.ps_n$i);  // ifc---ifc.ifc.p (CP)\n";
    }
#connect_by_name : ifc.ifc---port (SS), ifc.ifc---sig (SS)
    elsif($i > 150 && $i <= 200) {
	print FH "    c$k\_$zero.b$k\_$zero.ifc$m\_$zero.ifc$j\_$zero.connect_by_name(c$k\_$zero.a$k\_$one.p_z$i,pa_z$i);  // ifc.ifc---port (SS)\n";
print FH "    c$k\_$zero.b$k\_$zero.ifc$m\_$zero.ifc$j\_$one.connect_by_name(c$k\_$zero.a$k\_$two.s_z$i);  // ifc.ifc---sig (SS)\n";
    }
#connect_by_name : ifc.ifc---ifc.p (PC)
    elsif($i > 200 && $i <= 250) {
	print FH "    ifc$m\_$one.ifc$j\_$one.connect_by_name(c$k\_$zero.b$k\_$zero.ifc$j\_$zero.ps_m$i);  // ifc.ifc---ifc.p (PC)\n";
    }
#connect_by_name : ifc---ifc (CP) (object that doesn't exist)
    elsif($i > 250 && $i <= 300) {
	print FH "    c$k\_$zero.ifc$j\_$zero.connect_by_name(ifc$j\_$two);  // ifc---ifc (CP) (object that doesn't exist)\n";
    }
#connect_by_name : ifc---concat expr (CP)
    elsif($i > 300 && $i <= 350) {
	print FH "    c$k\_$zero.b$k\_$zero.a$k\_$zero.ifc$j\_$zero.connect_by_name({s_x$i,s_y$i});  // ifc---concat expr (CP)\n";
	print FH "    c$k\_$zero.ifc$j\_$one.connect_by_name(~p_x$i);\n";
    }
#connect_by_name : ifc.ifc---repl expr (CP), ifc.ifc---op expr (CP)
    elsif($i > 350 && $i <= 400) {
	print FH "    c$k\_$zero.b$k\_$zero.ifc$m\_$zero.ifc$j\_$zero.connect_by_name({2{s_x$i,s_y$i}});  // ifc.ifc---repl expr (CP)\n";
	print FH "    c$k\_$zero.b$k\_$zero.ifc$m\_$zero.ifc$j\_$one.connect_by_name(~p_x$i);  // ifc.ifc---op expr (CP)\n";
    }
    print FH "  }\n";
    print FH "};\n\n";









}
