package com.fastpath.cslc.cslgen;

import static org.junit.jupiter.api.Assertions.assertTrue;

import org.junit.jupiter.api.Test;

class CslGenDesignPrintTest {

    @Test
    void printPreambleAndChild() {
        CslGenDesign d = new CslGenDesign("top");
        d.addChild(new CslGenBitrange(d, "b0"));
        StringBuilder out = new StringBuilder();
        d.appendPrintedCsl(out);
        String t = out.toString();
        assertTrue(t.startsWith("//Automatically generated by cGen_ao.cpp\n"));
        assertTrue(t.contains("//Legal test case"));
        assertTrue(t.contains("csl_bitrange"));
    }
}
